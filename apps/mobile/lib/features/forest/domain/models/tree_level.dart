String _treeName(int level) {

  switch (level) {

    case 1:
      return "새싹";

    case 2:
      return "풀";

    case 3:
      return "묘목";

    case 4:
      return "어린나무";

    case 5:
      return "큰나무";

    case 6:
      return "울창한 나무";

    case 7:
      return "거목";

    case 8:
      return "신목";

    case 9:
      return "숲의 수호목";

    default:
      return "생명의 나무";

  }

}
